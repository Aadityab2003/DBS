CREATE DATABASE Const;

USE Const;


CREATE TABLE SAMPLE
(
C_ID INT PRIMARY KEY,
C_Name Varchar(60) NOT NULL,
C_Number INT UNIQUE
);


Create TABLE Person
(
P_ID INT PRIMARY KEY,
P_Name Varchar(60) NOT NULL,
P_Number INT UNIQUE,
C_ID INT, FOREIGN KEY (C_ID) REFERENCES SAMPLE(C_ID)
  );

